# list all numbers below 1000
# find multiples of 3 and 5
# sum the multiples

count = 0

total_count = 0

while count < 1000:

    if count % 5 == 0:

        total_count += count
    elif count % 3 == 0:

        total_count += count
    count += 1
print(total_count)

